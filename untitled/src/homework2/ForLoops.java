package homework2;

public class ForLoops {
    public static void main(String[] args) {
        // 1 - 100

        for (int index = 1; index <= 100; index++){
            System.out.println(index);
        }

        for (int index = 100; index >= 0; index--){
            System.out.println(index);
        }
    }
}
